// Class KonversiSuhu
class KonversiSuhu {
    // Method untuk mengubah Celcius ke Fahrenheit
    public double celciusToFahrenheit(double celcius) {
        return (celcius * 9/5) + 32;
    }

    // Method untuk mengubah Celcius ke Reamur
    public double celciusToReamur(double celcius) {
        return celcius * 4/5;
    }
}
